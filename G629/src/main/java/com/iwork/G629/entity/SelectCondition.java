package com.iwork.G629.entity;

public class SelectCondition {
	private String conditionId;
	private String conditionItemId;
	private String conditionName;
	private String conditionTip;
	private String conditionStart;
	private String conditionFinal;

	public String getConditionId() {
		return conditionId;
	}

	public void setConditionId(String conditionId) {
		this.conditionId = conditionId;
	}

	public String getConditionItemId() {
		return conditionItemId;
	}

	public void setConditionItemId(String conditionItemId) {
		this.conditionItemId = conditionItemId;
	}

	public String getConditionName() {
		return conditionName;
	}

	public void setConditionName(String conditionName) {
		this.conditionName = conditionName;
	}

	public String getConditionTip() {
		return conditionTip;
	}

	public void setConditionTip(String conditionTip) {
		this.conditionTip = conditionTip;
	}

	public String getConditionStart() {
		return conditionStart;
	}

	public void setConditionStart(String conditionStart) {
		this.conditionStart = conditionStart;
	}

	public String getConditionFinal() {
		return conditionFinal;
	}

	public void setConditionFinal(String conditionFinal) {
		this.conditionFinal = conditionFinal;
	}
}
